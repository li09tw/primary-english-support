-- 管理員消息表
CREATE TABLE IF NOT EXISTS admin_messages (
  id TEXT PRIMARY KEY,
  title TEXT NOT NULL,
  content TEXT NOT NULL,
  is_published BOOLEAN NOT NULL DEFAULT TRUE,
  is_pinned BOOLEAN NOT NULL DEFAULT FALSE, -- 釘選狀態
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

-- 遊戲方法表
CREATE TABLE IF NOT EXISTS game_methods (
  id TEXT PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  categories TEXT NOT NULL, -- JSON 格式，包含分類如：單字學習、句型練習、口語訓練
  grade1 BOOLEAN NOT NULL DEFAULT FALSE, -- 1年級
  grade2 BOOLEAN NOT NULL DEFAULT FALSE, -- 2年級
  grade3 BOOLEAN NOT NULL DEFAULT FALSE, -- 3年級
  grade4 BOOLEAN NOT NULL DEFAULT FALSE, -- 4年級
  grade5 BOOLEAN NOT NULL DEFAULT FALSE, -- 5年級
  grade6 BOOLEAN NOT NULL DEFAULT FALSE, -- 6年級
  materials TEXT NOT NULL, -- JSON 格式
  instructions TEXT NOT NULL, -- JSON 格式
  is_published BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

-- 學習輔助表
CREATE TABLE IF NOT EXISTS teaching_aids (
  id TEXT PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  category TEXT NOT NULL,
  file_url TEXT,
  is_published BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

-- 教材表
CREATE TABLE IF NOT EXISTS textbooks (
  id TEXT PRIMARY KEY,
  name TEXT NOT NULL,
  publisher TEXT NOT NULL,
  grade TEXT NOT NULL,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

-- 單元表
CREATE TABLE IF NOT EXISTS units (
  id TEXT PRIMARY KEY,
  textbook_id TEXT NOT NULL,
  name TEXT NOT NULL,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  FOREIGN KEY (textbook_id) REFERENCES textbooks(id) ON DELETE CASCADE
);

-- 單字表
CREATE TABLE IF NOT EXISTS vocabulary (
  id TEXT PRIMARY KEY,
  unit_id TEXT NOT NULL,
  english TEXT NOT NULL,
  chinese TEXT NOT NULL,
  phonetic TEXT NOT NULL,
  example TEXT NOT NULL,
  image TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL,
  FOREIGN KEY (unit_id) REFERENCES units(id) ON DELETE CASCADE
);

-- ========================================
-- LEARNING CONTENT SCHEMA TABLES
-- ========================================

-- 1. Grades table - Stores grade levels
CREATE TABLE IF NOT EXISTS grades (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL UNIQUE
);

-- 2. Sentence patterns table - Stores sentence patterns for different grades
CREATE TABLE IF NOT EXISTS sentence_patterns (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    grade_id INTEGER NOT NULL,
    pattern_text TEXT NOT NULL,
    pattern_type TEXT NOT NULL CHECK(pattern_type IN ('Question', 'Answer', 'Statement')),
    notes TEXT,
    FOREIGN KEY (grade_id) REFERENCES grades(id) ON DELETE CASCADE
);

-- 3. Question-Answer pairs table - Links questions to their answers
CREATE TABLE IF NOT EXISTS question_answer_pairs (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    question_pattern_id INTEGER NOT NULL,
    answer_pattern_id INTEGER NOT NULL,
    FOREIGN KEY (question_pattern_id) REFERENCES sentence_patterns(id) ON DELETE CASCADE,
    FOREIGN KEY (answer_pattern_id) REFERENCES sentence_patterns(id) ON DELETE CASCADE,
    UNIQUE(question_pattern_id, answer_pattern_id)
);

-- 4. Word themes table - Stores categories for words
CREATE TABLE IF NOT EXISTS word_themes (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL UNIQUE,
    description TEXT
);

-- 5. Words table - Main dictionary of words with has_plural boolean field
CREATE TABLE IF NOT EXISTS words (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    english_singular TEXT NOT NULL,
    english_plural TEXT,
    chinese_meaning TEXT NOT NULL,
    part_of_speech TEXT NOT NULL CHECK(part_of_speech IN ('noun', 'verb', 'adjective', 'adverb', 'preposition')),
    has_plural BOOLEAN NOT NULL DEFAULT 0
);

-- 6. Pattern slots table - Defines what kind of word each blank needs
CREATE TABLE IF NOT EXISTS pattern_slots (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    pattern_id INTEGER NOT NULL,
    slot_index INTEGER NOT NULL,
    required_part_of_speech TEXT NOT NULL,
    FOREIGN KEY (pattern_id) REFERENCES sentence_patterns(id) ON DELETE CASCADE
);

-- 7. Word theme associations table - Many-to-many relationship between words and themes
CREATE TABLE IF NOT EXISTS word_theme_associations (
    word_id INTEGER NOT NULL,
    theme_id INTEGER NOT NULL,
    PRIMARY KEY (word_id, theme_id),
    FOREIGN KEY (word_id) REFERENCES words(id) ON DELETE CASCADE,
    FOREIGN KEY (theme_id) REFERENCES word_themes(id) ON DELETE CASCADE
);

